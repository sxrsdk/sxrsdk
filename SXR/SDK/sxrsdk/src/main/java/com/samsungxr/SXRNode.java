    /**
     * Get the current {@link SXRRenderData}.
     *
     * @return The current {@link SXRRenderData rendering data}. If no rendering
     *         data is currently attached to the object, returns {@code null}.
     */
    public SXRRenderData getRenderData() {
        return (SXRRenderData) getComponent(SXRRenderData.getComponentType());
    }

    /**
     * Checks if this {@link SXRNode} has mesh. This method is not recursive.
     * That is, if it doesn't have a mesh though its children have, it returns
     * {@code false}.
     *
     * @return true if this {@link SXRNode} contains mesh itself.
     */
    public boolean hasMesh() {
        SXRRenderData rdata = getRenderData();
        return rdata != null && rdata.getMesh() != null;
    }

